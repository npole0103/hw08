#include "calculator.h"

// function that add integers a and b
int iAdd(int a, int b) {
    return a+b; //add
}

// function that subtract integers a and b
int iSub(int a, int b) {
    return a-b; //sub
}

// function that multiply integers a and b
int iMul(int a, int b) {
    return a*b; //mul
}

int iDiv(int a, int b) {
    return a/b; //div
}
